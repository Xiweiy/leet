SELECT DISTINCT STADIUM.id, STADIUM.date, STADIUM.people    ##DISTINCT
FROM STADIUM 
JOIN (
    SELECT ID FROM (
        SELECT ID, @COUNT := IF(@PREV=ID-1, @COUNT+1, 1) AS TIMES, @PREV := ID
        FROM (SELECT * FROM Stadium WHERE people>=100) A, (SELECT @COUNT:=0, @PREV:=NULL) B    ##>=
        ) C
    WHERE C.TIMES>=3 
    ) D
ON STADIUM.ID=D.ID OR STADIUM.ID=D.ID-1 OR STADIUM.ID=D.ID-2
